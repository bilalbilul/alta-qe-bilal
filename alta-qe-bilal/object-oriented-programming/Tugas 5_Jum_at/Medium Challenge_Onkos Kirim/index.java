public class index {
    int p;
    int l;
    int t;
    int kg;

    public void setP(int p) {
        this.p = p;
    }

    public void setL(int l) {
        this.l = l;
    }

    public void setT(int t) {
        this.t = t;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }

    public int getP() {
        return p;
    }

    public int getL() {
        return l;
    }

    public int getT() {
        return t;
    }

    public int getKg(){
        return kg;
    }

}
